package day05_variables;

public class ConateExample {

    public static void main(String[] args) {

        System.out.println(5+4);
        System.out.println(5+4 + "hello");// int+int -> int so 9(int) ->"hello(String) ->String -> output: 9hello

        System.out.println("hello" +5+4);// String +int -> hello54
    }
}
