package day07_unary_operators;

public class IncrementExample {
    public static void main(String[] args) {

        int x = 0;
        System.out.println(x);

        x++; // post Increment
        System.out.println(x);

        x++; // post Increment
        System.out.println(x);

        ++x; // pre Increment
        System.out.println(x);



    }
}
