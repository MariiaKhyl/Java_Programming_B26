package day08_reletional_operators;

public class OperatorPractice {
    public static void main(String[] args) {

        int b = 10;
        b++;
        System.out.println(b);
        b--;
        System.out.println(b);
        System.out.println(b++);
        System.out.println(b++);
        System.out.println(b);

    }





}
